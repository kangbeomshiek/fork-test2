package com.javalec.function;

public class Calc {

	
				//Method 
				//덧셈
				public int addAction(int first , int second) {
					return ( first + second);
				}
				//뺄셈
				public int subAction(int first , int second) {
					return ( first - second);
				}
				//곱셈
				public int mulAction(int first , int second) {
					return ( first * second);
				}
				//나눗셈
				public double divAction(int first , int second) {
					return ( (double)first / second);
				}
}	
